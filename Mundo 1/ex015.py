#Conversor de temperatura de Celsius para Fahrenheit e Kelvin V1

Celsius = float(input("Digite a temperatura em graus Celsius ")) #Tipo var que armazena o dado digitado no tipo number float
Fahrenheit = Celsius * 1.8 + 32 # Formula para obter dados em Fahrenheit
Kelvin = Celsius + 273 # Formula para obter dados em Kelvin
print(f"A temperatura em fahrenheit é: {Fahrenheit:.1f} e em Kelvin:{Kelvin}") #Resultado Final 

